package com.yunbo.mode;

public class XFData {
	private String type; 
	private String com_url; 
	private String com_cookie; 
	private String xf_url;
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getCom_url() {
		return com_url;
	}
	public void setCom_url(String com_url) {
		this.com_url = com_url;
	}
	public String getCom_cookie() {
		return com_cookie;
	}
	public void setCom_cookie(String com_cookie) {
		this.com_cookie = com_cookie;
	}
	public String getXf_url() {
		return xf_url;
	}
	public void setXf_url(String xf_url) {
		this.xf_url = xf_url;
	}
}
